#define N 5

int main () {
	//Inicio las variables
	int Arreglo[N];
	int i,num,j,aux;
	int *pnt,*pnt2;

	for(i = 0; i < N; i++) {
		printf("Ingrese un numero: ");
		scanf("%d", &Arreglo[i]);
	}

	printf("\nIngrese un numero: ");
	scanf("%d", &num); //Le doy valor a la variable num

	for(i = 0; i < N; i++) {
		if(num == Arreglo[i]) {
			for(j = i; j < N-1; j++) {
				pnt = &Arreglo[j]; //Guardo la direccion
				pnt2 = &Arreglo[j + 1];
				aux = *pnt;
				*pnt = *pnt2;
				*pnt2 = aux;

			}
		}
	}
	for(i = 0; i < N; i++) {
		printf("%d\n", Arreglo[i]);
	}
	return 0;
}